window.addEventListener('DOMContentLoaded', () => {
    let scrollPos = 0;
    const mainNav = document.getElementById('mainNav');
    const headerHeight = mainNav.clientHeight;
    const logoEmpresa = document.querySelector('#logoEmpresa');
    window.addEventListener('scroll', function() {
        const currentTop = document.body.getBoundingClientRect().top * -1;
        if ( currentTop < scrollPos) {
            // Scrolling Up
            if (currentTop > 0 && mainNav.classList.contains('is-fixed')) {
                mainNav.classList.add('is-visible');
                logoEmpresa.setAttribute('src','http://localhost/assets/img/club-algar-negro.png');
            } else {
                console.log(123);
                mainNav.classList.remove('is-visible', 'is-fixed');
                logoEmpresa.setAttribute('src','http://localhost/assets/img/club-algar-blanco.png')
            }
        } else {
            // Scrolling Down
            mainNav.classList.remove(['is-visible']);
            if (currentTop > headerHeight && !mainNav.classList.contains('is-fixed')) {
                mainNav.classList.add('is-fixed');
            }
        }
        scrollPos = currentTop;
    });
})